interface RollingGalleryProps {
  autoplay?: boolean;
  pauseOnHover?: boolean;
  images?: string[];
}

const RollingGallery: React.FC<RollingGalleryProps> = ({
  autoplay = false,
  pauseOnHover = false,
  images = [],
}) => {
  // Use default images if none are provided
  const galleryImages = images.length > 0 ? images : IMGS;
  const [isScreenSizeSm, setIsScreenSizeSm] = useState<boolean>(false);

  useEffect(() => {
    // Initialize screen size
    setIsScreenSizeSm(window.innerWidth <= 640);
    
    const handleResize = () => setIsScreenSizeSm(window.innerWidth <= 640);
    window.addEventListener("resize", handleResize);
    return () => window.removeEventListener("resize", handleResize);
  }, []);

  // 3D geometry calculations
  const cylinderWidth: number = isScreenSizeSm ? 1100 : 1800;
  const faceCount: number = galleryImages.length;
  const faceWidth: number = (cylinderWidth / faceCount) * 1.5;
  const radius: number = cylinderWidth / (2 * Math.PI);

  // Framer Motion values and controls
  const dragFactor: number = 0.05;
  const rotation = useMotionValue(0);
  const controls = useAnimation();

  // Create a 3D transform based on the rotation motion value
  const transform = useTransform(
    rotation,
    (val: number) => `rotate3d(0,1,0,${val}deg)`
  );

  // Wrap startInfiniteSpin in useCallback
  const startInfiniteSpin = useCallback((initialAngle: number) => {
    controls.start({
      rotateY: [initialAngle, initialAngle + 360],
      transition: {
        duration: 20,
        ease: "linear",
        repeat: Infinity,
        repeatType: "loop",
      },
    });
  }, [controls]);

  // Remove unused handleMouseDown or use it somewhere
  // const handleMouseDown = (e: React.MouseEvent<HTMLDivElement>): void => {
  //   controls.stop();
  // };

  // Fix the type signatures for handleDrag and handleDragEnd
  const handleDrag = (_: MouseEvent | TouchEvent | PointerEvent, info: PanInfo): void => {
    rotation.set(rotation.get() + info.delta.x * dragFactor);
  };
  
  const handleDragEnd = (_: MouseEvent | TouchEvent | PointerEvent, info: PanInfo): void => {
    const finalAngle = rotation.get() + info.velocity.x * dragFactor;
    rotation.set(finalAngle);
    if (autoplay) {
      startInfiniteSpin(finalAngle);
    }
  };
  
  // Replace img with Next.js Image component:
  {galleryImages.map((src, i) => {
    const angle = (i / faceCount) * 360;
    const z = radius * Math.cos((angle * Math.PI) / 180);
    const x = radius * Math.sin((angle * Math.PI) / 180);
    
    return (
      <motion.div
        key={i}
        className="absolute origin-center"
        style={{
          width: faceWidth,
          height: "100%",
          transform: `translate3d(${x}px, 0, ${z}px) rotateY(${angle}deg)`,
          transformStyle: "preserve-3d",
        }}
      >
        <div className="w-full h-full overflow-hidden rounded-lg">
          <Image
            src={src}
            alt={`Gallery image ${i + 1}`}
            width={500}
            height={300}
            className="w-full h-full object-cover"
          />
        </div>
      </motion.div>
    );
  })};
  // Add the handleUpdate function
    const handleUpdate = useCallback(() => {
      // This function is called on every animation frame
      // You can use it to sync the rotation value with the animation
    }, []);
  
  const handleMouseEnter = (): void => {
    if (autoplay && pauseOnHover) {
      controls.stop();
    }
  };

  const handleMouseLeave = (): void => {
    if (autoplay && pauseOnHover) {
      const currentAngle = rotation.get();
      startInfiniteSpin(currentAngle);
    }
  };

  return (
    <div className="relative h-[500px] w-full overflow-hidden">
      <div
        className="absolute top-0 left-0 h-full w-[48px] z-10"
        style={{
          background:
            "linear-gradient(to left, rgba(0,0,0,0) 0%, #f5f5f7 100%)",
        }}
      />
      <div
        className="absolute top-0 right-0 h-full w-[48px] z-10"
        style={{
          background:
            "linear-gradient(to right, rgba(0,0,0,0) 0%, #f5f5f7 100%)",
        }}
      />
      <div className="flex h-full items-center justify-center [perspective:1000px] [transform-style:preserve-3d]">
        <motion.div
          drag="x"
          dragElastic={0}
          onDrag={handleDrag}
          onDragEnd={handleDragEnd}
          onMouseEnter={handleMouseEnter}
          onMouseLeave={handleMouseLeave}
          animate={controls}
          onUpdate={handleUpdate} // Add this line to use the handleUpdate function
          style={{
            transform: transform,
            rotateY: rotation,
            width: cylinderWidth,
            transformStyle: "preserve-3d",
          }}
          className="flex min-h-[200px] cursor-grab items-center justify-center [transform-style:preserve-3d]"
        >
          {galleryImages.map((url, i) => (
            <div
              key={i}
              className="group absolute flex h-fit items-center justify-center p-[8%] [backface-visibility:hidden] md:p-[6%]"
              style={{
                transform: `rotateY(${(i * 360) / faceCount}deg) translateZ(${radius}px)`,
                width: faceWidth,
              }}
            >
              <Image
                src={url}
                alt={`Gallery image ${i + 1}`}
                width={500}
                height={300}
                className="h-auto w-full rounded-lg object-cover shadow-md transition-all duration-300 group-hover:scale-[1.02] group-hover:shadow-lg"
              />
            </div>
          ))}
        </motion.div>
      </div>
    </div>
  );
};

export default RollingGallery;